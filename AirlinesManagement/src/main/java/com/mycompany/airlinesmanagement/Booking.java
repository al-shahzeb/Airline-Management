    private void flightSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_flightSearchActionPerformed
        // TODO add your handling code here:
        
        
        String Arrival=arrival.getSelectedItem().toString();
        String Departure=departure.getSelectedItem().toString();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedatabase","root","2580");
            java.sql.Statement s = conn.createStatement();
            preSt = conn.prepareStatement("select * from flights where Arrival=? && Departure=?");
            preSt.setString(1,Arrival);
            preSt.setString(2, Departure);
            ResultSet rs = preSt.executeQuery();
            ResultSetMetaData metaData = rs.getMetaData();
            int colCnt = metaData.getColumnCount();
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setRowCount(0);
            fare.setText("");
            total.setText("");
            while(rs.next()){
                Vector vector = new Vector();
                for(int i=1; i<=colCnt; i++){
                    vector.add(rs.getString("FlightID"));
                    vector.add(rs.getString("FlightName"));
                    vector.add(rs.getString("Arrival"));
                    vector.add(rs.getString("Departure"));
                    vector.add(rs.getString("Date"));
                }
                model.addRow(vector);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Booking.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Booking.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_flightSearchActionPerformed

