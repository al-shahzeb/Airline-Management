     public void autoId() throws ClassNotFoundException, SQLException{
        Class.forName("com.mysql.jdbc.Driver");
        conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedatabase","root","2580");
        java.sql.Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("Select MAX(TicketID) from tickets");
        rs.next();
        if(rs.getString("MAX(TicketID)")==null)
            ticketid.setText("TK001");
        else{
            long id = Long.parseLong(rs.getString("MAX(TicketID)").substring(2,rs.getString("MAX(TicketID)").length()));
            id++;
            ticketid.setText("TK"+String.format("%03d", id));
        }
    }
