    public void autoId() throws ClassNotFoundException, SQLException{
        Class.forName("com.mysql.jdbc.Driver");
        conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedatabase","root","2580");
        java.sql.Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("Select MAX(customersID) from customers");
        rs.next();
        if(rs.getString("MAX(customersID)")==null)
            customerId.setText("CS001");
        else{
            long id = Long.parseLong(rs.getString("MAX(customersID)").substring(2,rs.getString("MAX(customersID)").length()));
            id++;
            customerId.setText("CS"+String.format("%03d", id));
        }
    }
