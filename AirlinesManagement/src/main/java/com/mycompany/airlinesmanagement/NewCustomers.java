    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
         //JOptionPane.showMessageDialog(null,"New customer added.");
         
        String cid = customerId.getText();
        String firstName=fName.getText();
        String lastName = lName.getText();
        String pid = pID.getText();
        String nid = nID.getText();
        String adr = address.getText();
        String cont = contact.getText();
        String gender = "";
        if(male.isSelected())
            gender+="Male";
        else
            gender+="Female";
        DateFormat df = new SimpleDateFormat("yyyy-dd-mm");
        String date = df.format(dob.getDate());
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewCustomers.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedatabase","root","2580");
        } catch (SQLException ex) {
            Logger.getLogger(NewCustomers.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            preSt = conn.prepareStatement("insert into customers(customersID,FirstName,LastName,PassportID,NicID,Address,DOB,Gender,ContactNo)values(?,?,?,?,?,?,?,?,?)");
            preSt.setString(1, cid);
            preSt.setString(2, firstName);
            preSt.setString(3, lastName);
            preSt.setString(4, pid);
            preSt.setString(5, nid);
            preSt.setString(6, adr);
            preSt.setString(7, date);
            preSt.setString(8, gender);
            preSt.setString(9, cont);
            preSt.executeUpdate();
            JOptionPane.showMessageDialog(this,"New customer added.");
        } catch (SQLException ex) {
            Logger.getLogger(NewCustomers.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_saveButtonActionPerformed

